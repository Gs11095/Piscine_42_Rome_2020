#include <unistd.h>

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int		main(int argc, char **argv)
{
	int k;

	k = 1;
	while (k <= (argc - 1))
	{
		ft_putstr(argv[k]);
		k++;
	}
	return (0);
}
