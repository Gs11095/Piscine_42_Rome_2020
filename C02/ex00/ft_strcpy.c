#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int k;

	k = 0;
	while (src[k] != '\0')
	{
		dest[k] = src[k];
		k++;
	}
	dest[k] = '\0';
	return (dest);
}
