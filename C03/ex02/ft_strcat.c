#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	int k;
	int j;

	k = 0;
	j = 0;
	while (dest[k] != '\0')
		k++;
	while (src[j] != '\0')
	{
		dest[k + j] = src[j];
		j++;
	}
	dest[k + j] = '\0';
	return (dest);
}
