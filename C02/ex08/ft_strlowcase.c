#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int k;

	k = 0;
	while (str[k])
	{
		if (str[k] >= 65 && str[k] <= 90)
		{
			str[k] = str[k] + 32;
		}
		k++;
	}
	return (str);
}
