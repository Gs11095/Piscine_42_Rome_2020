#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int k;

	k = 0;
	while (str[k])
	{
		if (str[k] < 65 || str[k] > 90)
		{
			return (0);
		}
		k++;
	}
	return (1);
}
